def add(x, y):
    return x + y

def sub(x, y):
    return x - y
   
def mult(x, y):
    return x * y

def div(x, y):
    return x / y

   
print("The sum of 10 + 15 is equal to ", add(10, 15))
print("The difference of 10 - 15 is equal to ", sub(10, 15))
print("The product of 10 x 15 is equal to ", mult(10, 15))
print("The output of 10 / 15 is equal to ", div(10, 15))
